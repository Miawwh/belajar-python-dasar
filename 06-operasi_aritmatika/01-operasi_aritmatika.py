# Mari kita coba satu per satu:

a = 10
b = 3

# Operasi penjumlahan (+) :
print(f"{a} + {b} = {a+b}")

# Operasi pengurangan (-) :
print(f"{a} - {b} = {a-b}")

# Operasi perkalian (*) :
print(f"{a} * {b} = {a*b}")

# Operasi pembagian (/) :
print(f"{a} / {b} = {a/b}")

# Operasi pembagian bulat (//) :
print(f"{a} // {b} = {a//b}")

# Operasi modulus (%) :
print(f"{a} % {b} = {a%b}")

# Operasi perpangkatan (**) :
print(f"{a} ** {b} = {a**b}")