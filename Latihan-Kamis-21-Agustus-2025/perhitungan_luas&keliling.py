input_lebar = int(input("Masukan nilai untuk lebarnya = "))
input_panjang = int(input("Masukan nilai untuk panjangnya = "))

rumus_luas_persegi_panjang = input_panjang * input_lebar
rumus_keliling_persegi_panjang = 2 * (input_panjang + input_lebar)

print(f"Luas persegi panjang = {rumus_luas_persegi_panjang}")
print(f"Keliling persegi panjang = {rumus_keliling_persegi_panjang}")