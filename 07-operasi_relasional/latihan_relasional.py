# Latihan 1: Cek ganjil/genap
n = int(input("Masukkan bilangan bulat: "))
is_genap = (n % 2 == 0)
print(f"{n} genap? -> {is_genap}")

# Latihan 2: Cek rentang umur
umur = int(input("Masukkan umur Anda: "))
# Misal: dewasa = umur >= 17
print("Dewasa (umur >= 17)? ->", umur >= 17)
